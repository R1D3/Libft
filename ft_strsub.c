#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char			*newstr;
	unsigned int	i;

	if (!s)
		return (NULL);
	i = 0;
	newstr = ft_strnew(len);
	if (!newstr)
		return (NULL);
	while (i < len)
	{
		newstr[i] = s[start];
		i++;
		start++;
	}
	newstr[i] = '\0';
	return (newstr);
}
