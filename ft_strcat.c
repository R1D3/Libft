#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int size_dest;

	size_dest = ft_strlen(dest);
	i = 0;
	while (src[i] != '\0')
	{
		dest[size_dest + i] = src[i];
		i++;
	}
	dest[size_dest + i] = '\0';
	return (dest);
}
